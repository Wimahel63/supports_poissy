// --1 :incrementation
var nb1 = 1;
nb1=nb1 + 1;// j'augmente de 1
console.log(nb1);

// ecriture simplifiée
nb1++; // le premier resultat de l'operation du dessus donne 2, donc si on rajoute encore 1, ça donne 3, ici le resultat affiché sera donc 3. le ++ ajoute donc 1 à chaque fois
console.log(nb1);

// ecriture simplifiée
++nb1;
// affichage => nb1 = 4
console.log(nb1);

// --2 : decrementation:
nb1 = nb1 - 1;
console.log(nb1);

// ecriture simplifiée
nb1--;
console.log(nb1);
